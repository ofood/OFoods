
namespace OFoods.Specifications
{
    /// <summary>
    /// 代表排序.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// 指示未指定排序样式.
        /// </summary>
        Unspecified = -1,
        /// <summary>
        /// 表示升序排序.
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// 表示降序排序.
        /// </summary>
        Descending = 1
    }
}
